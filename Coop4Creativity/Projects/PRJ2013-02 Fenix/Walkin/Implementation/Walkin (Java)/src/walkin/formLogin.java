    /** Action to check the username and password. */
    @Action
    public void loginAction() {
        /* locals. */
        String username;
        String password;

        /* get the info. */
        username = this.userTextField.getText();
        password = new String(this.passwordTextField.getPassword());

        /* here we check the inserted user name and password. */
        try {

            /* create temporary user for password validation. */
            bUser user = new bUser();
            user.setUsername(username);
            user.setPassword(password);

            /* validate user. */
            if (Main.getServiceLayer().getUserService().validateUser(user)) {
                dispose();
                return;
            }
        } catch (Exception ex) {
        }

        /* if we get to this point then the username and password
         * did not check out. */

        messageLabel.setText("Utilizador ou password inv√°lidos.");
        messageLabel.setForeground(Color.red);

    }
